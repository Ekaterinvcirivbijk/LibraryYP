    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (tableAppeals.getSelectedRow() > -1)
        {
            int row = tableAppeals.getSelectedRow();
            Appeal app = (Appeal)tableAppeals.getValueAt(row, 0);
            if (!app.isReturned())
            {
                AppealDialog dialog = new AppealDialog(parentFrame);
                dialog.fill(app);
                dialog.setVisible(true);
                if (dialog.isSaved())
                {
                    app = dialog.getAppeal();
                    List list = modelAppeals.getList();
                    list.set(row, app);
                    modelAppeals.setList(list);
                    modelAppeals.fireTableDataChanged();
                    tableAppeals.repaint();
                    tableAppeals.setRowSelectionInterval(row, row);
                    if (app.isReturned())
                    {
                        jButton1.setText(String.format("%.2f", ((MainForm)parentFrame).getLibrary().getProfit()));
                        ((MainForm)parentFrame).getBookPanel().refresh();
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


