    private void init()
    {
        modelAppeals = new AppealTableModel(list);
        tableAppeals = new JTable(modelAppeals);
        jScrollPane1.setViewportView(tableAppeals);
        
        tableAppeals.setRowHeight(30);
        tableAppeals.getColumnModel().getColumn(0).setMaxWidth(0);
        tableAppeals.getColumnModel().getColumn(0).setMinWidth(0);
        tableAppeals.getColumnModel().getColumn(0).setPreferredWidth(0);
        
        ButtonHeaderRenderer bhRenderer = new ButtonHeaderRenderer();
        for (int i = 0; i < tableAppeals.getColumnModel().getColumnCount(); i++)
            tableAppeals.getColumnModel().getColumn(i).setHeaderRenderer(bhRenderer);
        tableAppeals.getTableHeader().setReorderingAllowed(false);
        
        Renderer renderer = new Renderer();
        CheckBoxRenderer chRenderer = new CheckBoxRenderer();
        for (int i = 0; i < tableAppeals.getColumnModel().getColumnCount(); ++i)
            if (tableAppeals.getColumnClass(i).equals(Boolean.class))
                tableAppeals.getColumnModel().getColumn(i).setCellRenderer(chRenderer);
            else
                tableAppeals.getColumnModel().getColumn(i).setCellRenderer(renderer);
    }
    
    public void refresh()
    {
        modelAppeals.fireTableDataChanged();
        tableAppeals.repaint();
        jButton1.setText(String.format("%.2f", ((MainForm)parentFrame).getLibrary().getProfit()));
    }
    
