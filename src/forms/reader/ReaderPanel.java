    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ReaderDialog dialog = new ReaderDialog(parentFrame);
        Reader r = new Reader();
        dialog.fill(r);
        dialog.setVisible(true);
        if (dialog.isSaved())
        {
            r = dialog.getReader();
            List list = modelReaders.getList();
            list.add(r);
            modelReaders.setList(list);
            modelReaders.fireTableDataChanged();
            tableReaders.repaint();
            int row = 0;
            for(int i = 0; i < tableReaders.getRowCount(); ++i)
            if (tableReaders.getValueAt(i, 0) == r)
            {
                row = i;
                break;
            }
            tableReaders.setRowSelectionInterval(row, row);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (tableReaders.getSelectedRow() > -1)
        {
            ReaderDialog dialog = new ReaderDialog(parentFrame);
            int row = tableReaders.getSelectedRow();
            Reader r = (Reader)tableReaders.getValueAt(row, 0);
            dialog.fill(r);
            dialog.setVisible(true);
            if (dialog.isSaved())
            {
                r = dialog.getReader();
                List list = modelReaders.getList();
                list.set(sorter.convertRowIndexToModel(row), r);
                modelReaders.setList(list);
                modelReaders.fireTableDataChanged();
                tableReaders.repaint();
                row = 0;
                for(int i = 0; i < tableReaders.getRowCount(); ++i)
                if (tableReaders.getValueAt(i, 0) == r)
                {
                    row = i;
                    break;
                }
                tableReaders.setRowSelectionInterval(row, row);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (tableReaders.getSelectedRow() > -1)
        {
            int row = tableReaders.getSelectedRow();
            List list = modelReaders.getList();
            list.remove(sorter.convertRowIndexToModel(row));
            modelReaders.setList(list);
            modelReaders.fireTableDataChanged();
            tableReaders.repaint();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


