    private void init()
    {
        modelReaders = new ReaderTableModel(list);
        tableReaders = new JTable(modelReaders);
        jScrollPane1.setViewportView(tableReaders);
        
        tableReaders.setRowHeight(30);
        tableReaders.getColumnModel().getColumn(0).setMaxWidth(0);
        tableReaders.getColumnModel().getColumn(0).setMinWidth(0);
        tableReaders.getColumnModel().getColumn(0).setPreferredWidth(0);
        
        ButtonHeaderRenderer bhRenderer = new ButtonHeaderRenderer();
        for (int i = 0; i < tableReaders.getColumnModel().getColumnCount(); i++)
            tableReaders.getColumnModel().getColumn(i).setHeaderRenderer(bhRenderer);
        tableReaders.getTableHeader().setReorderingAllowed(false);
        
        Renderer renderer = new Renderer();
        for (int i = 0; i < tableReaders.getColumnModel().getColumnCount(); ++i)
            tableReaders.getColumnModel().getColumn(i).setCellRenderer(renderer);
        sorter = new TableRowSorter<TableModel>(modelReaders);
        tableReaders.setRowSorter(sorter);
        RowSorter.SortKey main = new RowSorter.SortKey(1, SortOrder.ASCENDING);
        sorter.toggleSortOrder(main.getColumn());
    }

    public Reader getSelectedReader()
    {
        if (tableReaders.getSelectedRow() == -1)
            return null;
        else
            return (Reader)tableReaders.getValueAt(tableReaders.getSelectedRow(), 0);
    }
    
