    private void init()
    {
        modelBooks = new BookTableModel(list);
        tableBooks = new JTable(modelBooks);
        jScrollPane1.setViewportView(tableBooks);
        
        tableBooks.setRowHeight(30);
        tableBooks.getColumnModel().getColumn(0).setMaxWidth(0);
        tableBooks.getColumnModel().getColumn(0).setMinWidth(0);
        tableBooks.getColumnModel().getColumn(0).setPreferredWidth(0);
        
        ButtonHeaderRenderer bhRenderer = new ButtonHeaderRenderer();
        for (int i = 0; i < tableBooks.getColumnModel().getColumnCount(); i++)
            tableBooks.getColumnModel().getColumn(i).setHeaderRenderer(bhRenderer);
        tableBooks.getTableHeader().setReorderingAllowed(false);
        
        Renderer renderer = new Renderer();
        CheckBoxRenderer chRenderer = new CheckBoxRenderer();
        for (int i = 0; i < tableBooks.getColumnModel().getColumnCount(); ++i)
            if (tableBooks.getColumnClass(i).equals(Boolean.class))
                tableBooks.getColumnModel().getColumn(i).setCellRenderer(chRenderer);
            else
                tableBooks.getColumnModel().getColumn(i).setCellRenderer(renderer);
        sorter = new TableRowSorter<TableModel>(modelBooks);
        tableBooks.setRowSorter(sorter);
        RowSorter.SortKey sortByAuthor = new RowSorter.SortKey(2, SortOrder.ASCENDING);
        RowSorter.SortKey sortByName = new RowSorter.SortKey(1, SortOrder.ASCENDING);
        sorter.toggleSortOrder(sortByName.getColumn());
        sorter.toggleSortOrder(sortByAuthor.getColumn());
    }

    public void refresh()
    {
        modelBooks.fireTableDataChanged();
        tableBooks.repaint();
    }
    
    public Book getSelectedBook()
    {
        if (tableBooks.getSelectedRow() == -1)
            return null;
        else
            return (Book)tableBooks.getValueAt(tableBooks.getSelectedRow(), 0);
    }
    
