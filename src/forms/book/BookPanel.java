    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        BookDialog dialog = new BookDialog(parentFrame);
        Book b = new Book();
        dialog.fill(b);
        dialog.setVisible(true);
        if (dialog.isSaved())
        {
            b = dialog.getBook();
            List list = modelBooks.getList();
            list.add(b);
            modelBooks.setList(list);
            modelBooks.fireTableDataChanged();
            tableBooks.repaint();
            int row = 0;
            for(int i = 0; i < tableBooks.getRowCount(); ++i)
            if (tableBooks.getValueAt(i, 0) == b)
            {
                row = i;
                break;
            }
            tableBooks.setRowSelectionInterval(row, row);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (tableBooks.getSelectedRow() > -1)
        {
            BookDialog dialog = new BookDialog(parentFrame);
            int row = tableBooks.getSelectedRow();
            Book b = (Book)tableBooks.getValueAt(row, 0);
            dialog.fill(b);
            dialog.setVisible(true);
            if (dialog.isSaved())
            {
                b = dialog.getBook();
                List list = modelBooks.getList();
                list.set(sorter.convertRowIndexToModel(row), b);
                modelBooks.setList(list);
                modelBooks.fireTableDataChanged();
                tableBooks.repaint();
                
                row = 0;
                for(int i = 0; i < tableBooks.getRowCount(); ++i)
                if (tableBooks.getValueAt(i, 0) == b)
                {
                    row = i;
                    break;
                }
                tableBooks.setRowSelectionInterval(row, row);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (tableBooks.getSelectedRow() > -1)
        {
            int row = tableBooks.getSelectedRow();
            List list = modelBooks.getList();
            list.remove(sorter.convertRowIndexToModel(row));
            modelBooks.setList(list);
            modelBooks.fireTableDataChanged();
            tableBooks.repaint();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


